<h1 class="mb-4 text-xl font-medium text-mlq-text">About Donna</h1>

<h2 class="mt-6 mb-2 text-sm font-medium tracking-wide text-mlq-muted uppercase">What is Donna?</h2>

<p class="mb-3 max-w-prose text-sm leading-relaxed text-mlq-text">
	Donna is a friendly, focused frontend built on top of <strong>LQ-AI</strong>, an open-source legal
	operating system. Where LQ-AI exposes the full power of a legal AI platform, Donna presents a
	streamlined experience designed for day-to-day legal work — clean, approachable, and tuned for the
	tasks that matter most.
</p>

<p class="mb-3 max-w-prose text-sm leading-relaxed text-mlq-text">
	The <strong>Powered by LQ-AI</strong> callout at the top of every page in this guide links to a deeper
	explanation of the backend that drives Donna's answers, document intelligence, and knowledge retrieval.
</p>

<h2 class="mt-6 mb-2 text-sm font-medium tracking-wide text-mlq-muted uppercase">
	A tour of the sidebar
</h2>

<p class="mb-3 max-w-prose text-sm leading-relaxed text-mlq-text">
	Everything in Donna is reachable from the left sidebar. Here is what each entry does:
</p>

<ul class="mb-3 ml-4 list-disc space-y-1 text-sm text-mlq-text">
	<li>
		<strong>Assistant</strong> — the main chat interface; start a conversation, attach files, and apply
		skills or prompts from your library.
	</li>
	<li>
		<strong>Projects</strong> — organise your work by matter; chats and documents can be scoped to a project
		so context stays together.
	</li>
	<li>
		<strong>Workflows</strong> — skills, playbooks, saved prompts, and automations that run or accelerate
		recurring tasks.
	</li>
	<li>
		<strong>Tabular</strong> — structured document review; extract and compare fields across a set of
		files in a grid.
	</li>
	<li><strong>About</strong> — this guide.</li>
	<li><strong>Settings</strong> — account, model routing, and provider configuration.</li>
</ul>

<h2 class="mt-6 mb-2 text-sm font-medium tracking-wide text-mlq-muted uppercase">
	How this guide is organised
</h2>

<p class="mb-3 max-w-prose text-sm leading-relaxed text-mlq-text">
	The rail on the left of this guide has one page per area of Donna. Start with the
	<strong>Assistant</strong> page — it covers the core chat loop that underlies everything else.
	From there, work through <strong>Projects</strong>, <strong>Workflows</strong>,
	<strong>Automations</strong>, and <strong>Tabular</strong> in any order that suits your workflow.
	The <strong>Knowledge</strong>,
	<strong>Models</strong>, and <strong>Trust &amp; citations</strong> pages cover the infrastructure behind
	Donna's answers.
</p>

